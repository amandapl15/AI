        # Return the policy for each (N,S,W,E) state
        if self.mdp.isTerminal(state):
            return None
        # Get the first action
        maximAction = self.mdp.getPossibleActions(state)[0]
        # Init of the Q calue
        q = self.getQValue(state, maximAction)
        # Check all the Possible Actions
        for action in self.mdp.getPossibleActions(state):
            aux = self.getQValue(state, action)
            if q < aux:
                q = aux
                maximAction = action
        # Return just the best action
        return maximAction

